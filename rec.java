public class Rectangle
{
	private int length;
	private int width;
	
	Rectangle ( )
	{
		length = width = 0;
	}
	
	public int getLength ( )
	{
		return this.length;
	}
	
	public	int getWidth ( )
	{
		return this.width;
	}
		
	public	void setLength ( int len )
	{
		this.length = len;
	}
	
	public	void setWidth ( int width )
	{
		this.width = width;
	}

}


		
